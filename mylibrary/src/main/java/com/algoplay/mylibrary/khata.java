package com.algoplay.mylibrary;

public class khata {
    String khataname;
    String khatasize;
    String khatatime;

    public khata(String khataname, String khatasize, String khatatime) {
        this.khataname = khataname;
        this.khatasize = khatasize;
        this.khatatime = khatatime;
    }

    public String getKhataname() {
        return khataname;
    }

    public void setKhataname(String khataname) {
        this.khataname = khataname;
    }

    public String getKhatasize() {
        return khatasize;
    }

    public void setKhatasize(String khatasize) {
        this.khatasize = khatasize;
    }

    public String getKhatatime() {
        return khatatime;
    }

    public void setKhatatime(String khatatime) {
        this.khatatime = khatatime;
    }
}
